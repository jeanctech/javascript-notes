// Variables en JavaScript

//Variable Var

var variable_1 = 10;
var variable_2 = 20;
var variable_3 = variable_1 + variable_2;

console.log(variable_3)

//Variables Let

let let_1 = 10;
let let_2 = 20;
let let_3 = let_1 + let_2;

console.log(let_3)

//Variables Const

const const_1 = 10;
const const_2 = 20;
const const_3 = const_1 + const_2;

console.log(const_3)